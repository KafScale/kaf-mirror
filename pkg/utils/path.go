package utils

import (
	"os"
	"path/filepath"
)

// ProjectRoot finds the root directory of the project.
// It does this by looking for the go.mod file.
func ProjectRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		return "."
	}

	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}

		parent := filepath.Dir(dir)
		if parent == dir {
			return "."
		}
		dir = parent
	}
}
